const REGEX = {
  NUMBER: /^[\d,]+$/,
  PERCENT: /^(100(\.0{1})?|[0-9]{1,2}(\.[0-9]{1})?)$/,
  USERNAME: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)[a-zA-Z\d]*$/,
  PASSWORD:
    /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*])[a-zA-Z\d!@#$%^&*]+$/,
  NUMERIC_INPUT: /^[0-9]*(\.[0-9]*)?(,[0-9]*)?$/,
};

export { REGEX };
